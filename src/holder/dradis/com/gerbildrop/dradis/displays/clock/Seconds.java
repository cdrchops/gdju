    public static int[] checkSeconds(int second) {
        int seconds[] = new int[2];
        if (second > 9) {
            if (second > 49) {
                seconds[0] = 5;
                seconds[1] = second - 50;
            } else if (second > 39) {
                seconds[0] = 4;
                seconds[1] = second - 40;
            } else if (second > 29) {
                seconds[0] = 3;
                seconds[1] = second - 30;
            } else if (second > 19) {
                seconds[0] = 2;
                seconds[1] = second - 20;
            } else {
                seconds[0] = 1;
                seconds[1] = second - 10;
            }
        } else {
            seconds[0] = 0;
            seconds[1] = second;
        }

        return seconds;
    }
