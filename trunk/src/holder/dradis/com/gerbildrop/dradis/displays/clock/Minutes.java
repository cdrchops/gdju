    public static int[] checkMinutes(int minute) {
        int minutes[] = new int[2];
        if (minute > 9) {
            if (minute > 49) {
                minutes[0] = 5;
                minutes[1] = minute - 50;
            } else if (minute > 39) {
                minutes[0] = 4;
                minutes[1] = minute - 40;
            } else if (minute > 29) {
                minutes[0] = 3;
                minutes[1] = minute - 30;
            } else if (minute > 19) {
                minutes[0] = 2;
                minutes[1] = minute - 20;
            } else {
                minutes[0] = 1;
                minutes[1] = minute - 10;
            }
        } else {
            minutes[0] = 0;
            minutes[1] = minute;
        }

        return minutes;
    }
